package com.ocean.submersibleProbe.model;

public class InitializationRequest {

	    private Probe probe;
	    private Grid grid;

	    public Probe getProbe() {
	        return probe;
	    }

	    public void setProbe(Probe probe) {
	        this.probe = probe;
	    }

	    public Grid getGrid() {
	        return grid;
	    }

	    public void setGrid(Grid grid) {
	        this.grid = grid;
	    }
}
